var ouiMu sync.RWMutex

// LookupVendor returns the vendor name for a MAC address prefix.
// Checks embedded DB first, then queries macvendors.com API and caches the result.
func LookupVendor(mac string) string {
	mac = strings.ToUpper(strings.ReplaceAll(mac, "-", ":"))
	parts := strings.Split(mac, ":")
	if len(parts) < 3 {
		return ""
	}
	prefix := parts[0] + ":" + parts[1] + ":" + parts[2]

	ouiMu.RLock()
	vendor, ok := ouiDB[prefix]
	ouiMu.RUnlock()
	if ok {
		return vendor
	}

	// Online fallback
	vendor = lookupOnline(prefix)
	ouiMu.Lock()
	ouiDB[prefix] = vendor // cache even empty results to avoid re-querying
	ouiMu.Unlock()

	return vendor
}

func lookupOnline(prefix string) string {
	client := &http.Client{Timeout: 3 * time.Second}
	resp, err := client.Get("https://api.macvendors.com/" + prefix)
	if err != nil {
		return ""
	}
	defer resp.Body.Close()
	if resp.StatusCode != 200 {
		return ""
	}
	body, err := io.ReadAll(io.LimitReader(resp.Body, 256))
	if err != nil {
		return ""
	}
	vendor := strings.TrimSpace(string(body))
	if strings.Contains(vendor, "Not Found") {
		return ""
	}
	if vendor != "" {
		log.Info().Str("mac", prefix).Str("vendor", vendor).Msg("OUI online lookup")
	}
	return vendor
}

// ClassifyByVendor guesses device type from vendor name.
func ClassifyByVendor(vendor string) string {
	lower := strings.ToLower(vendor)
	switch {
	case strings.Contains(lower, "apple"):
		return "phone" // could be mac/phone/tablet - default phone for passive
	case strings.Contains(lower, "samsung"):
		return "phone"
	case strings.Contains(lower, "google"):
		return "media"
	case strings.Contains(lower, "amazon"):
		return "media"
	case strings.Contains(lower, "sonos"):
		return "media"
	case strings.Contains(lower, "roku"):
		return "media"
	case strings.Contains(lower, "lg electronics"):
		return "tv"
	case strings.Contains(lower, "sony"):
		return "tv"
	case strings.Contains(lower, "tp-link") || strings.Contains(lower, "netgear") ||
		strings.Contains(lower, "ubiquiti") || strings.Contains(lower, "cisco") ||
		strings.Contains(lower, "asus") || strings.Contains(lower, "aruba") ||
		strings.Contains(lower, "avm") || strings.Contains(lower, "fritz"):
		return "router"
	case strings.Contains(lower, "hp") || strings.Contains(lower, "hewlett") ||
		strings.Contains(lower, "brother") || strings.Contains(lower, "canon") ||
		strings.Contains(lower, "epson") || strings.Contains(lower, "xerox"):
		return "printer"
	case strings.Contains(lower, "raspberry"):
		return "server"
	case strings.Contains(lower, "espressif") || strings.Contains(lower, "tuya") ||
		strings.Contains(lower, "shelly") || strings.Contains(lower, "tasmota"):
		return "iot"
	case strings.Contains(lower, "ring") || strings.Contains(lower, "nest"):
		return "iot"
	case strings.Contains(lower, "intel") || strings.Contains(lower, "dell") ||
		strings.Contains(lower, "lenovo") || strings.Contains(lower, "microsoft") ||
		strings.Contains(lower, "fn-link") || strings.Contains(lower, "changwang"):
		return "desktop"
	case strings.Contains(lower, "vmware") || strings.Contains(lower, "virtual"):
		return "server"
	default:
		return ""
	}
}

